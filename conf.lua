
function love.conf(t)
    t.window.title = "Sprite Editor"
    t.window.resizable = true
    t.window.borderless = false
    t.window.fullscreen = false
    t.window.fullscreentype = "desktop"
    t.window.vsync = 1
    t.window.width = 800
    t.window.height = 600
    t.window.minwidth = 400
    t.window.minheight = 300
    t.console = true 
end